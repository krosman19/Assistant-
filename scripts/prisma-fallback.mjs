import fs from 'node:fs';
import path from 'node:path';

const command = process.argv[2];
const root = process.cwd();
const generatedDir = path.join(root, '.generated');
const dataDir = path.join(root, '.data');
const schemaPath = path.join(root, 'prisma', 'schema.prisma');

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true });
}

function readModels() {
  const schema = fs.readFileSync(schemaPath, 'utf8');
  return [...schema.matchAll(/^model\s+(\w+)/gm)].map((match) => match[1]);
}

if (command === 'generate') {
  ensureDir(generatedDir);
  const models = readModels();
  const output = `// Generated by scripts/prisma-fallback.mjs for registry-restricted demos.\n` +
    `// Replace with \`prisma generate\` when Prisma packages are installed.\n` +
    `export const models = ${JSON.stringify(models, null, 2)};\n`;
  fs.writeFileSync(path.join(generatedDir, 'prisma-client.mjs'), output);
  console.log(`Generated fallback Prisma model manifest for ${models.length} models at .generated/prisma-client.mjs`);
} else if (command === 'push') {
  ensureDir(dataDir);
  const models = readModels();
  const sql = models.map((model) => `-- Placeholder table for ${model}\nCREATE TABLE IF NOT EXISTS "${model}" ("id" TEXT PRIMARY KEY);`).join('\n\n');
  fs.writeFileSync(path.join(dataDir, 'schema.sql'), `${sql}\n`);
  console.log(`Wrote fallback schema plan for ${models.length} models to .data/schema.sql`);
} else {
  console.error('Usage: node scripts/prisma-fallback.mjs <generate|push>');
  process.exit(2);
}
